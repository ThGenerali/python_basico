curso = "Curso de Python"
frutas = ["laranja", "uva", "pera"]
saques = [750, 250]

#in
    #verifica o objeto na sequência
print("Python" in curso)
print("maça" in frutas)
print(200 in saques)

#not in
    #verifica objeto fora da sequência
print("Curso" not in curso)
print("goiaba" not in frutas)
print(750 not in saques)

