saldo = 500

#atribuição simples
saldo = saldo
print(saldo)

#atribuição com adição
saldo += 200
#saldo = saldo + 200
print(saldo)

#atribuição com subtração
saldo -= 200
#saldo = saldo - 200
print(saldo)

#atribuição com multiplicação
saldo *= 2
#saldo = saldo * 2
print(saldo)

#atribuição com divisão
saldo /= 2
#saldo = saldo / 50
print(saldo)
saldo //= 2
#saldo = saldo // 10
print(saldo)

#atribuição com módulo
saldo %= 4
#saldo = saldo % 4
print(saldo)

#atribuição com exponenciação
saldo **= 2
#saldo = saldo ** 2
print(saldo)