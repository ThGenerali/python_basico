nome = "Thiago Nascimento Mendes Aquinho Generali"

print(nome[0])
#podemos selecioanr uma letra por índice

print(nome[:6])
#start = 0 e stop = 6

print(nome[7:])
#start = 7 e não contém stop

print(nome[7:17])
#start = 7 e stop = 17

print(nome[18:24:2])
#start = 18, stop = 24 e step = 2

print(nome[:])
#demonstra a variável completa

print(nome[::-1])
#mostra a variável de trás pra frente